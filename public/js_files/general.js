////////////////////////////////////////////////////////////////////////
// 
// Will create an array from 1 to n where n is specifed in the input
//
//////////////////////////////////////////////////////////////////////// 
function newArrayFrom1toN(n)
  {
  var newArray = [];

  for(var i = 0; i <= n; i++)
    {
    newArray.push(i);
    }

  return newArray;
  }